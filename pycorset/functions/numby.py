"""
Module with function for adding numbers.
"""

def add(num_a, num_b):
    """
    Adds two numbers.

    Args:
        param1 (int): The first number.
        param2 (int): The second number.

    Returns:
        int: The sum of param1 and param2

    >>> add(1, 3)
    4
    """
    return num_a + num_b
